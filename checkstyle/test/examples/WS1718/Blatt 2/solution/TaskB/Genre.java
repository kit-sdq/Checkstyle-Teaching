/**
 * Represents a genre a {@link Series} can be associated with.
 * 
 * @author Joshua Gleitze
 * @version 1.0
 */
public enum Genre {
    /**
     * Adventure movies
     */
    ADVENTURE, 
    /**
     * Action movies
     */
    ACTION,
    /**
     * Thrillers
     */
    THRILLER,
    /**
     * Comedy
     */
    COMEDY;
}
